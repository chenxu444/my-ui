/**
 * 
 * @param {a} fn 目标执行函数
 * @param {*} duration 防抖时间
 * @returns 
 */
export function deBounce(fn,duration = 100){
    let timer
    return function(...args){
        clearTimeout(timer)
        timer = setTimeout(()=>{
            fn.apply(this,...args)
        },duration)
    }
}